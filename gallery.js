const videoExtensions = [".mp4", ".mov", ".webm", ".m4v"];

function isVideo(src) {
  return videoExtensions.some((extension) => src.toLowerCase().endsWith(extension));
}

function createDialog() {
  const dialog = document.createElement("div");
  dialog.className = "media-dialog";
  dialog.hidden = true;
  dialog.innerHTML = `
    <div class="media-dialog-panel">
      <div class="media-dialog-head">
        <h2></h2>
        <button type="button" aria-label="关闭">×</button>
      </div>
      <div class="media-grid"></div>
    </div>
  `;
  document.body.appendChild(dialog);
  dialog.addEventListener("click", (event) => {
    if (event.target === dialog || event.target.closest("button")) {
      dialog.hidden = true;
      document.body.classList.remove("dialog-open");
    }
  });
  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      dialog.hidden = true;
      document.body.classList.remove("dialog-open");
    }
  });
  return dialog;
}

function renderMediaItem(src) {
  const link = document.createElement("a");
  link.className = "media-tile";
  link.href = src;
  link.target = "_blank";
  link.rel = "noopener";

  if (isVideo(src)) {
    const video = document.createElement("video");
    video.src = src;
    video.muted = true;
    video.playsInline = true;
    video.preload = "metadata";
    link.appendChild(video);
    const badge = document.createElement("span");
    badge.className = "media-badge";
    badge.textContent = "▶";
    link.appendChild(badge);
  } else {
    const img = document.createElement("img");
    img.src = src;
    img.alt = "";
    img.loading = "lazy";
    link.appendChild(img);
  }

  return link;
}

function openGallery(dialog, category) {
  dialog.querySelector("h2").textContent = category.title;
  const grid = dialog.querySelector(".media-grid");
  grid.replaceChildren(...category.items.map(renderMediaItem));
  dialog.hidden = false;
  document.body.classList.add("dialog-open");
}

function renderCategory(category, dialog) {
  const button = document.createElement("button");
  button.className = "gallery-category";
  button.type = "button";
  button.innerHTML = `
    <img src="${category.cover}" alt="" loading="lazy" />
    <span>${category.title}</span>
  `;
  button.addEventListener("click", () => openGallery(dialog, category));
  return button;
}

document.querySelectorAll("[data-gallery-project]").forEach((container) => {
  const project = container.dataset.galleryProject;
  const dialog = createDialog();
  container.replaceChildren(...galleries[project].map((category) => renderCategory(category, dialog)));
});
